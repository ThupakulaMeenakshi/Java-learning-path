class ArithmeticOP {
    public static void main(String z[]) {
        int a = 6;
        int b = 5;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
        int result = a++; // post increment
        System.out.println(result);
        int result1 = ++a; // pre increment
        System.out.println(result1);

    }
}